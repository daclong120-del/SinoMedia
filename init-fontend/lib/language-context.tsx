export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>("en");

  useEffect(() => {
    const updateLang = () => {
      const stored = localStorage.getItem("preferred_language");
      if (stored) {
        setLanguageState(stored.startsWith("vi") ? "vi" : "en");
      } else if (localStorage.language) {
        setLanguageState(localStorage.language.startsWith("vi") ? "vi" : "en");
      }
    };

    const timer = setTimeout(updateLang, 0);

    const handleStorageChange = () => {
      const updated = localStorage.getItem("preferred_language");
      if (updated) {
        setLanguageState(updated.startsWith("vi") ? "vi" : "en");
      }
    };

    window.addEventListener("storage", handleStorageChange);
    return () => {
      clearTimeout(timer);
      window.removeEventListener("storage", handleStorageChange);
    };
  }, []);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    const fullCode = lang === "vi" ? "vi-VN" : "en-US";
    localStorage.setItem("preferred_language", fullCode);
    localStorage.language = lang;
    window.dispatchEvent(new Event("storage"));
  };

  const t = (key: string, section: string = "common"): string => {
    const sectDict = TRANSLATIONS[language]?.[section];
    if (sectDict && sectDict[key] !== undefined) {
      return sectDict[key];
    }
    return key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
